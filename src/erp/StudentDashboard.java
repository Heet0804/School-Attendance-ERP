    private void loadTodaysSchedule() {
        try {
            con = erp.DBConnection.connect();

            // Get today's day of week
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("EEEE");
            String today = sdf.format(new java.util.Date());

            String query = "SELECT subject, time_from, time_to FROM schedule WHERE grade = ? AND division = ? AND day_name = ? ORDER BY time_from";
            pst = con.prepareStatement(query);
            pst.setInt(1, LoginSession.grade);
            pst.setString(2, LoginSession.division);
            pst.setString(3, today);

            rs = pst.executeQuery();

            StringBuilder schedule = new StringBuilder();
            schedule.append("<html><b>Today's Schedule (").append(today).append("):</b><br><br>");

            boolean hasSchedule = false;
            while (rs.next()) {
                hasSchedule = true;
                String subject = rs.getString("subject");
                String timeFrom = rs.getString("time_from");
                String timeTo = rs.getString("time_to");

                // Format time
                String formattedFrom = formatTime(timeFrom);
                String formattedTo = formatTime(timeTo);

                schedule.append("â€¢ ").append(subject)
                        .append(" (").append(formattedFrom).append(" - ").append(formattedTo).append(")<br>");
            }

            if (!hasSchedule) {
                schedule.append("No classes scheduled for today.");
            }

            schedule.append("</html>");
            jLabel6.setText(schedule.toString());

            rs.close();
            pst.close();
            con.close();

        } catch (Exception e) {
            jLabel6.setText("<html>Error loading schedule</html>");
            e.printStackTrace();
        }
    }

    private String formatTime(String time) {
        try {
            java.text.SimpleDateFormat input = new java.text.SimpleDateFormat("HH:mm:ss");
            java.text.SimpleDateFormat output = new java.text.SimpleDateFormat("hh:mm a");
            return output.format(input.parse(time));
        } catch (Exception e) {
            return time;
        }
    }

    private void loadLatestNotices() {
    try {
        con = erp.DBConnection.connect();

        String query = "SELECT title, message, date FROM notices ORDER BY date DESC LIMIT 5";
        pst = con.prepareStatement(query);
        rs = pst.executeQuery();

        StringBuilder notices = new StringBuilder();
        notices.append("<html>");
        notices.append("<b>Latest Notices:</b><br><br>");

        boolean hasNotices = false;

        while (rs.next()) {
            hasNotices = true;

            String title = rs.getString("title");
            String message = rs.getString("message");

            // Optional: limit message length for UI safety
            if (message.length() > 100) {
                message = message.substring(0, 100) + "...";
            }

            notices.append("&#8226; "); // bullet
            notices.append("<b>").append(title).append(":</b> ");
            notices.append(message);
            notices.append("<br><br>");
        }

        if (!hasNotices) {
            notices.append("No notices available.");
        }

        notices.append("</html>");
        jLabel7.setText(notices.toString());

        rs.close();
        pst.close();
        con.close();

    } catch (Exception e) {
        jLabel7.setText("<html>Error loading notices</html>");
        e.printStackTrace();
    }
}


